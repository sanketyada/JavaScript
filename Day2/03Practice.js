//String Operation
let name="Sanket Yadav"

console.log(name.length);   //12
console.log(name.charAt(3))  //k
console.log(name.toUpperCase())  //SANKET YADAV
console.log(name.substring(1,4))  //ank
console.log(name.slice(0,4))  //Sank
console.log(name.trim())  //starting space would be ignored
console.log(name.includes('Sanket'))  //true
console.log(name.split('-'))  //converted str into array